CREATE TABLE GETPROJECT(
    GNO NUMBER(38) PRIMARY KEY,
    MEMAIL VARCHAR2(80) NOT NULL REFERENCES MEMBER(MEMAIL),
    PNO NUMBER(38) NOT NULL REFERENCES PROJECT(PNO),
