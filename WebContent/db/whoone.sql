    PCATEGORY VARCHAR2(100) NOT NULL,
    PACCOUNT VARCHAR2(100) NOT NULL,
    PBANKNAME VARCHAR2(100) NOT NULL,
    PACNAME VARCHAR(20) NOT NULL
